        <ol>
            <li>After clicking <strong>Start Health Check</strong>, the webcam feed will appear in the <strong>"Webcam Container."</strong></li>
            <li>Hold a tea leaf in front of the camera.</li>
            <li>The system will analyze the leaf and display the prediction results below the camera view. Each prediction will show:
                <ul>
                    <li><strong>Class Name</strong>: e.g., "Healthy" or "Diseased".</li>
                    <li><strong>Confidence Score</strong>: The accuracy of the prediction.</li>
                </ul>
            </li>
        </ol>

        <!-- Check Quality -->
        <h3>3. Check Quality</h3>
        <p>To analyze further details about the quality of the tea leaf, click on the <strong>"Check Quality"</strong> button. This will redirect you to another page for detailed quality inspection.</p>

        <!-- Navigation -->
        <h3>4. Navigation</h3>
        <p>Use the navigation bar at the top to access different sections of the Tea Quality App, such as Home, Inventory, Prediction, and Tracking.</p>

        <!-- Logout -->
        <h3>5. Logout</h3>
        <p>Click <strong>Logout</strong> when you are finished to securely end your session.</p>

        <!-- Troubleshooting -->
